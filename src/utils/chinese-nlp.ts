// ============================================================
// 长文本截断阈值
// ============================================================
const MAX_TEXT_LENGTH = 10000;

// ============================================================
// 1. 中文分词
// ============================================================

/**
 * 判断字符是否为中文字符
 */
function isChinese(char: string): boolean {
  const code = char.charCodeAt(0);
  return code >= 0x4e00 && code <= 0x9fff;
}

/**
 * 判断字符是否为英文字母或数字
 */
function isAlphanumeric(char: string): boolean {
  const code = char.charCodeAt(0);
  return (code >= 0x41 && code <= 0x5a) ||  // A-Z
         (code >= 0x61 && code <= 0x7a) ||  // a-z
         (code >= 0x30 && code <= 0x39);    // 0-9
}

/**
 * 正向最大匹配分词算法
 * 对中文部分使用词典匹配，对英文/数字部分保持连续，对未登录词使用单字切分
 *
 * @param text - 待分词文本
 * @returns 分词结果数组
 */
export function tokenize(text: string): string[] {
  if (!text || text.trim().length === 0) return [];

  // 长文本截断
  const input = text.length > MAX_TEXT_LENGTH ? text.slice(0, MAX_TEXT_LENGTH) : text;

  const tokens: string[] = [];
  let i = 0;

  while (i < input.length) {
    const char = input[i];

    // 跳过空白和标点
    if (/[\s\u3000]/.test(char)) {
      i++;
      continue;
    }

    // 中文标点跳过
    if (/[\u3001\u3002\uff0c\uff1b\uff1a\uff01\uff1f\u201c\u201d\u2018\u2019\u300a\u300b\u3010\u3011\uff08\uff09]/.test(char)) {
      i++;
      continue;
    }

    // 英文标点跳过
    if (/[.,;:!?'"()[\]{}<>/\\@#$%^&*+=~`|]/.test(char)) {
      i++;
      continue;
    }

    // 英文/数字连续片段
    if (isAlphanumeric(char)) {
      let j = i;
      while (j < input.length && isAlphanumeric(input[j])) {
        j++;
      }
      tokens.push(input.slice(i, j).toLowerCase());
      i = j;
      continue;
    }

    // 中文字符：正向最大匹配
    if (isChinese(char)) {
      let matched = false;
      // 从最大词长开始尝试匹配
      for (let len = Math.min(MAX_WORD_LEN, input.length - i); len > 1; len--) {
        const candidate = input.slice(i, i + len);
        if (DICT_SET.has(candidate)) {
          tokens.push(candidate);
          i += len;
          matched = true;
          break;
        }
      }
      // 未匹配到词典词，单字切分
      if (!matched) {
        tokens.push(char);
        i++;
      }
      continue;
    }

    // 其他字符（如日文、韩文等）单字处理
    tokens.push(char);
    i++;
  }

  return tokens;
}

// ============================================================
// 4. 情感倾向判断
// ============================================================

/**
 * 情感倾向分析（基于情感词典）
 *
 * @param text - 待分析文本
 * @returns 包含分数和标签的结果对象，score 范围 [-1, 1]
 */
export function sentimentAnalysis(text: string): { score: number; label: 'positive' | 'negative' | 'neutral' } {
  if (!text || text.trim().length === 0) {
    return { score: 0, label: 'neutral' };
  }

  const input = text.length > MAX_TEXT_LENGTH ? text.slice(0, MAX_TEXT_LENGTH) : text;
  const tokens = tokenize(input);

  if (tokens.length === 0) {
    return { score: 0, label: 'neutral' };
  }

  // 否定词列表
  const NEGATION_WORDS = new Set(['不', '没', '没有', '未', '非', '别', '莫', '勿', '无', '不是', '不会', '不能', '不太', '不怎么']);

  let positiveCount = 0;
  let negativeCount = 0;

  for (let i = 0; i < tokens.length; i++) {
    const token = tokens[i];
    const prevToken = i > 0 ? tokens[i - 1] : '';
    const isNegated = NEGATION_WORDS.has(prevToken);

    if (POSITIVE_WORDS.has(token)) {
      if (isNegated) {
        // 否定+正面词 = 负面（如"不好"）
        negativeCount++;
      } else {
        positiveCount++;
      }
    }
    if (NEGATIVE_WORDS.has(token)) {
      if (isNegated) {
        // 否定+负面词 = 正面（如"不差"）
        positiveCount++;
      } else {
        negativeCount++;
      }
    }
  }

  const total = positiveCount + negativeCount;

  // 没有情感词则判定为中性
  if (total === 0) {
    return { score: 0, label: 'neutral' };
  }

  // 分数范围 [-1, 1]
  const score = (positiveCount - negativeCount) / total;

  let label: 'positive' | 'negative' | 'neutral';
  if (score > 0.1) {
    label = 'positive';
  } else if (score < -0.1) {
    label = 'negative';
  } else {
    label = 'neutral';
  }

  return { score: Math.round(score * 1000) / 1000, label };
}
